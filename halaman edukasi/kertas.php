      <p>Sampah kertas dapat menimbulkan dampak negatif terhadap lingkungan dan kesehatan manusia. Dampak-dampak tersebut antara lain:</p>
      <ul>
        <li>Pembuangan sampah kertas yang tidak sesuai prosedur dapat mencemari air dan tanah.</li>
        <li>Sampah kertas yang terbakar dapat menghasilkan gas-gas berbahaya bagi kesehatan.</li>
        <li>Pembuangan sampah kertas yang tidak terkelola dengan baik dapat memperburuk masalah pencemaran lingkungan.</li>
        <li>Pembuatan kertas memerlukan sumber daya alam seperti kayu, air, dan energi, sehingga dapat menimbulkan dampak negatif pada lingkungan.</li>
      </ul>
