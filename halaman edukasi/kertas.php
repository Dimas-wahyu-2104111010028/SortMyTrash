    <section id="pengelolaan">
      <h2>Pengelolaan Sampah Kertas</h2>
      <p>Berikut cara pengolaan sampah yaitu membuat paper bag mudah dan cepat.</p>
      <ol>
        <li>Alat :  
            <ul>- Gunting</ul>
            <ul>- Pembolong kertas</ul> 
        <ul>-Double tape/ lem kertas</ul></li>
        <li>Bahan:
            <ul>- Kertas kado</ul>
            <ul>- Kertas karton</ul> 
            <ul>- Tali goni atau tali benang</ul></li>
        <li>cara membuat paper bag:
           <ul>
            <li>Potong kertas kado berbentuk persegi panjang (ukuran bisa Anda sesuaikan dengan barang muatan Anda  dengan ketentuan panjangnya kira-kira 2 x barang dan lebar 1,5 x barang), lalu, lipat menjadi dua bagian dan sisakan kira-kira 2 cm untuk menempelkan lem.</li>
            <li>Tempelkan lem pada sisi yang sudah disisakan tadi, lalu rekatkan kedua sisi kertas.</li>
            <li>Lipat bagian bawah untuk membuat alas paper bag, kira-kira 10-15 cm, dan buat garis lipatan tegas</li>
            <li>Buka lipatan kertas, lalu lipat sisi sebelah kanan dan kiri ke dalam sampai membentuk segitiga di sisi sebelah kanan dan kiri untuk membuat bentuk samping nyata paper bag</li>
            <li>Ambil potongan kertas karton berbentuk persegi panjang atau kira-kira 10 cm, sesuai dengan ukuran alas paper bag</li>
            <li>Oleskan lem kertas di salah satu sisinya, selipkan kertas karton ke dalam paper bag. Lalu, beri lem kertas di sekeliling alas dan rekatkan</li>
            <li>Lipat sisi kanan dan kiri paper bag kira-kira sepanjang 2 cm, mengikuti garis pendanda di bagian alas</li>
            <li>Buka lipatan, lalu lipat sesuai dengan garis yang terbentuk</li>
           <li>Lipat bagian atas sepanjang 5 cm ke bagian dalam, lubangi bagian atas, lalu buat gagang tas dengan menggunakan tali goni atau tali benang.
      </ol>
