        Adapun produk yang dihasilkan dari daur ulang kertas meliputi kertas cetak putih, kertas toilet dan tisu, handuk kertas dan serbet, kartu ucapan, karton, majalah, koran, serta beberapa bahan kerajinan tangan.
        Salah satu contoh kerajinan yang bisa di olah dari kertas kado yaitu paper bag.</p>
