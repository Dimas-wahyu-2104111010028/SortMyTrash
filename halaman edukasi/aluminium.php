		<h2>Apa itu Sampah Aluminium?</h2>
		<p>Sampah aluminium adalah limbah padat yang terbuat dari logam aluminium atau bahan yang mengandung aluminium. Bahan aluminium dapat didaur ulang dan digunakan kembali untuk membuat berbagai produk, seperti kaleng minuman, kerangka jendela, dan bagian kendaraan.</p>

		<h2>Cara Mendaur Ulang Sampah Aluminium</h2>
		<p>Proses daur ulang aluminium melibatkan proses peleburan dan pemurnian bahan aluminium bekas untuk membuat aluminium yang baru. Proses ini jauh lebih efisien dan ramah lingkungan dibandingkan dengan proses pembuatan aluminium baru dari bijih bauksit. Cara mendaur ulang sampah aluminium meliputi:</p>
		<ul>
			<li>Memilah aluminium dari sampah rumah tangga</li>
			<li>Membawa aluminium bekas ke tempat daur ulang</li>
			<li>Melebur dan memurnikan aluminium bekas untuk membuat aluminium yang baru</li>
			<li>Menggunakan aluminium yang didaur ulang untuk membuat produk baru</li>
		</ul>

		<h2>Manfaat Mendaur Ulang Sampah Aluminium</h2>
		<p>Daur ulang aluminium memiliki beberapa manfaat, antara lain:</p>
		<ul>
			<li>Mengurangi jumlah sampah yang dibuang di tempat pembuangan akhir</li>
			<li>Mengurangi penggunaan energi dan bahan baku untuk produksi aluminium baru</li>
			<li>Menjaga lingkungan dengan mengurangi polusi dan dampak lingkungan negatif dari proses pembuatan aluminium baru</li>
			<li>Menghemat sumber daya alam yang terbatas</li>
		</ul>
